// Backbone-independent rotamer library by Dunbrack group
// where the rotamer instances are sorted by their probabilities

// For detailed description, refer to the following article
// Dunbrack Jr., R.L., Cohen, F.E.: Bayesian statistical analysis of protein side-chain rotamer
// preferences. Protein Science 6(8), 1661{1681 (1997)

struct BBIndRotamer{
	ResidueCode	 code                              ;
	char         chiRotamer[ 4 ]                   ; // 0~6
	short	       numOfObservationsForChiOneRotamers; // 0~8585
	short	       numOfObservations                 ; // 0~8585
	rg_REAL      probability                       ;
	rg_REAL      stdDeviation                      ;
	rg_REAL      condProbabilityGivenChiOneRotamer ;
	rg_REAL      stdDeviationGivenChiOneRotamer    ;
	rg_REAL      chi[ 4 ]                          ;
	rg_REAL      stdDeviationOfChi[ 4 ]            ;	
	
	BBIndRotamer()
	{
		code = UNK_RESIDUE;
		numOfObservationsForChiOneRotamers = 0;
		numOfObservations =	0;
		probability = 0.0;
		stdDeviation = 0.0;
		condProbabilityGivenChiOneRotamer = 0.0;
		stdDeviationGivenChiOneRotamer = 0.0;
		for (rg_INDEX i = 0;i < 4;i++)
		{
			chi[ i ] = 0.0;
			stdDeviationOfChi[ i ] = 0.0;
		}				
	}
};

